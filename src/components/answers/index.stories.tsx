        "<p>It's futile to build windowless controls <strong>and</strong> fit them into Delphi's VCL framework.</p>\n\n<p>You bring up Internet Explorer as an example. But in that case, it's entirely in charge of everything that resides on it. It has its own internal notion of what the active control is, but think about what it looks like from the outside: It's just one giant control. When you ask the OS what has focus, the single browser control has it, no matter which of the browser's subcontrols <em>appears</em> to have focus.</p>\n\n<p>When you press Tab, it looks to the OS as though the browser has simply consumed a tab character, just like edit controls do. Edit controls move the cursor over a few spaces and add tab characters to their internal buffers; browser controls move the cursor to another region of the display.</p>\n\n<p>You're thinking of doing all this on a Delphi TForm. Delphi forms already have a framework for managing the active control and handling keystrokes, and you're going to have to fight it all. If you want windowless controls, go the Internet Explorer route and <strong>build your own container control</strong> to hold them so that you can remain in charge of everything that happens inside it.</p>\n\n<p>Your container can be a VCL control, but the things you put on it probably can't â€” they'll still be expecting to use the VCL focus- and keyboard-handling rules. Notice how you can't put ordinary Windows controls in Internet Explorer, either. Anything you put there needs to go through specific ActiveX interfaces. Maybe you'll need interfaces, too, or maybe you can just make your own set of control classes that descend from some special ancestor class you design to work with your container. Don't start with <code>TGraphicControl</code>; it's too entrenched in the VCL to be usable as the basis for your offshoot control library.</p>\n\n<p>It will be a lot of work, but then again, so was Internet Explorer.</p>\n",
